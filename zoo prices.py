""" this program determines whether you pay the child price at the zoo or not """

# setting child age limit
CHILD_AGE = 13

# asking for user input
age = int(input("How old are you?\n"))

# seeing if the user is younger than CHILD_AGE
if age < CHILD_AGE:
    print("You pay the child price!")
 
